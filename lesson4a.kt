fun main() {
    // functions are a block of statements that perform a given task
    // A function is introduced by the use of the fun key word, followed by the name of the function, parenthesis and the body of the function come last which is introduced by the use of curly braces.
    fun sayHello () {
        println("Hello there, Hope you are having a good day?")
    } 


    // Below we invoke/call our function
    sayHello()

    // create an example of a function and invoke/call it
    fun example () {
        println("This is the best day ever!")
    }
    example()
    
}